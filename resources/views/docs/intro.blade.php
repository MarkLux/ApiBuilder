    <h3>
        1. 什么是Api Leaf?
    </h3>
    <hr>
    <p>
        Api Leaf 是一个以Api接口文档为核心，提供接口测试、文档管理等功能的开发者辅助工具。
    </p>
    <br>
    <h3>
        2. Api Leaf可以帮助开发者做什么？
    </h3>
    <hr>
    <p>
        对于后台接口的开发人员，Api Leaf可以帮助你：
        <ul>
            <li>进行HTTP接口测试（类似POSTMAN）</li>
            <li>基于测试结果自动生成API文档</li>
            <li>方便的管理整个项目的所有API</li>
            <li>快速地添加测试人员和其他开发者一起维护项目文档</li>
        </ul>
    </p>
    <p>
        对于阅读文档的前端/客户端开发人员，Api Leaf可以帮助你：
        <ul>
            <li>获得全面、舒适的文档阅读体验</li>
            <li>快速查找数据结构和字段定义</li>
        </ul>
    </p>
    <br>
    <h3>
        3. Api Leaf适用于什么样的团队和场景？
    </h3>
    <hr>
    <p>
        Api Leaf主要适用于敏捷开发的中小型项目和团队，能够节省大量的文档编写时间，提升效率。
    </p>
    <p>
        目前支持所有基于JSON交换数据的HTTP接口。
    </p>
