    <h3>
        1. 共享文档编辑权
    </h3>
    <hr>
    <p>
        当一个项目有多个开发人员共同开发时，就需要共享项目文档的编辑权，以便所有的接口人员都能测试接口，并且生成完整的项目文档。
    </p>
    <br>
    <h3>
        2. 管理项目成员
    </h3>
    <hr>
    <p>
        只有项目的创建者可以管理项目的成员，在主面板 > 我创建的项目 列表中，点击 <b>管理成员</b> 即可进入项目的成员管理界面：
        <img src="http://of1deuret.bkt.clouddn.com/18-4-22/63254345.jpg" class="img-responsive" alt="Responsive image" style="max-width: 50vw">
        输入新成员在Api Leaf注册时使用的邮箱即可将其添加进项目中。
    </p>
